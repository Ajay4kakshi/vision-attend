export type UserRole = "teacher" | "student" | "parent";

export interface User {
  id: string;
  name: string;
  email: string;
  role: UserRole;
  department?: string;
  registerNumber?: string;
  className?: string;
  childName?: string;
  avatar?: string;
}

export interface Student {
  id: string;
  name: string;
  registerNumber: string;
  className: string;
  status: "present" | "absent";
  lastSeen: string;
  attendance: number;
  email: string;
  phone: string;
}

export interface AttendanceRecord {
  date: string;
  status: "present" | "absent" | "scheduled";
}

export interface Recognition {
  studentName: string;
  time: string;
  location: string;
}

export interface Notification {
  id: string;
  title: string;
  message: string;
  time: string;
  read: boolean;
  type: "info" | "warning" | "success";
}
